package com.domain.app.dto;

public class PlexusParams {

    private String plexusUserName;
    private String plexusUserKey;
    private String platformName;

    public String getPlexusUserName() {
        return plexusUserName;
    }

    public void setPlexusUserName(String plexusUserName) {
        this.plexusUserName = plexusUserName;
    }

    public String getPlexusUserKey() {
        return plexusUserKey;
    }

    public void setPlexusUserKey(String plexusUserKey) {
        this.plexusUserKey = plexusUserKey;
    }

    public String getPlatformName() {
        return platformName;
    }

    public void setPlatformName(String platformName) {
        this.platformName = platformName;
    }
}
